CLevel* CLevel::NewLC()
	{
	CLevel* self = new (ELeave) CLevel();
	CleanupStack::PushL(self);
	self->ConstructL();
	return self;
	}

////////////////////////////////////////////////////////////////////////

CLevel* CLevel::NewL()
	{
	CLevel* self = CLevel::NewLC();
	CleanupStack::Pop();
	return self;
	}

